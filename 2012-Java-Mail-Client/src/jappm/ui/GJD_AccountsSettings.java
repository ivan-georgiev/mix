    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO in progress


        ArrayList<JAPPAccountSettings> result = new ArrayList();

        DefaultListModel dm = (DefaultListModel) this.jList1.getModel();


        for (int i = 0; i < dm.getSize(); i++) {

            if ("New Account".equals(((JAPPAccountSettings) dm.getElementAt(i)).accountName)) {
                continue;
            }

            result.add((JAPPAccountSettings) dm.getElementAt(i));
        }


        for (Integer i : accountsToDelete) {
            parent.controller.deleteAccount(i);
        }

        parent.controller.setJAPPSettings(result);

        DefaultTreeModel model = (DefaultTreeModel) parent.jTree1.getModel();
        model.setRoot(parent.controller.getTreeFromNode(1));
        parent.repaint();
        
         JOptionPane.showMessageDialog(this, "Commit completed!", "Info", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jButton1ActionPerformed

