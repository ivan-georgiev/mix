    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged


        JAPPAccountSettings jps = (JAPPAccountSettings) this.jList1.getSelectedValue();

        if (jps == null) {
            return;
        }

        //TODO fill all fields

        this.jTextField11.setText(jps.accountName);
        this.jTextField1.setText(jps.name);
        this.jTextField2.setText(jps.email);
        //
        this.jTextField4.setText(jps.serverIn);
        this.jTextField5.setText(Integer.toString(jps.serverInPort));
        this.jTextField6.setText(jps.userIn);
        this.jPasswordField1.setText("");

        if (!jps.passwordIn.isEmpty()) {
            this.jPasswordField1.setText(jps.passwordIn);
            this.jCheckBox3.setSelected(true);
        }

        //
        this.jTextField7.setText(jps.serverOut);
        this.jTextField8.setText(Integer.toString(jps.serverOutPort));
        this.jPasswordField2.setText("");

        if (jps.userOut.isEmpty()) {
            this.jTextField9.setText("");
            this.jPasswordField2.setText("");
            this.jCheckBox5.setSelected(false);
            this.jCheckBox6.setSelected(false);
            this.jCheckBox6.setEnabled(false);

        } else {
            this.jCheckBox5.setSelected(true);
            this.jTextField9.setText(jps.userOut);

            if (!jps.passwordOut.isEmpty()) {
                this.jPasswordField2.setText(jps.passwordOut);
                this.jCheckBox7.setSelected(true);
            }
        }

//        if (!jps.userIn.isEmpty() && !jps.userIn.isEmpty() && !(jps.userIn.equals(jps.userOut))) {
//            this.jCheckBox6.setSelected(false);
//            this.jTextField9.setEnabled(true);
//            this.jPasswordField2.setEnabled(true);
//
//        }


        if (jps.serverInIsSSL) {
            this.jCheckBox2.setSelected(true);
        } else {
            this.jCheckBox2.setSelected(false);
        }

        if (jps.serverOutIsSSL) {
            this.jCheckBox4.setSelected(true);
        } else {
            this.jCheckBox4.setSelected(false);
        }


        if (jps.serverInTypeId == 1) {
            this.jComboBox1.setSelectedIndex(0);
        } else {
            this.jComboBox1.setSelectedIndex(1);
        }


        if ("New Account".equals(jps.accountName)) {
            jComboBox1.setEnabled(true);
        } else {
            jComboBox1.setEnabled(false);
        }

        jTabbedPane1.setVisible(true);


    }//GEN-LAST:event_jList1ValueChanged

