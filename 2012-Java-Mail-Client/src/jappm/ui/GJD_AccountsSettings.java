    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed


        JAPPAccountSettings jps = (JAPPAccountSettings) this.jList1.getSelectedValue();
        if (jps == null) {
            return;
        }


        //TODO fill all fields
        jps.accountName = jTextField11.getText();

        if (jps.accountName.equals("New Account")) {

            JOptionPane.showMessageDialog(this, "Can not save account with default name");

            return;
        }


        jps.name = jTextField1.getText();
        jps.email = jTextField2.getText();
        //
        jps.serverIn = jTextField4.getText();
        jps.serverInPort = Integer.parseInt(jTextField5.getText());
        jps.userIn = jTextField6.getText();
        //
        if (this.jCheckBox3.isSelected()) {
            jps.passwordIn = this.jPasswordField1.getText();
        }

        jps.serverOut = jTextField7.getText();
        jps.serverOutPort = Integer.parseInt(jTextField8.getText());
        jps.userOut = this.jTextField9.getText();

        if (!this.jCheckBox5.isSelected()) {
            jps.userOut = "";
            jps.passwordOut = "";
        } else if (this.jCheckBox6.isSelected()) {
            jps.userOut = jps.userIn;
            jps.passwordOut = jps.passwordIn;
        }


        jps.serverInIsSSL = this.jCheckBox2.isSelected();

        jps.serverOutIsSSL = this.jCheckBox4.isSelected();


        String inServerType = (String) jComboBox1.getSelectedItem();

        if (inServerType.equalsIgnoreCase("imap")) {
            jps.serverInTypeId = 1;
        } else {
            jps.serverInTypeId = 2;
        }

        jComboBox1.setEnabled(false);


        this.jList1.repaint();

    }//GEN-LAST:event_jButton5ActionPerformed

