    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed



        DefaultListModel dm = (DefaultListModel) this.jList1.getModel();
        DefaultListModel dm2 = (DefaultListModel) this.jList2.getModel();


        for (int i = 0; i < dm2.getSize(); i++) {

            if ("New Address".equals(((JAPPAddress) dm.getElementAt(i)).email)) {

                return;
            }

        }

        JAPPAddress ac = new JAPPAddress();

        dm.addElement(ac);
        dm2.addElement(ac);

        this.jList1.setSelectedIndex(dm.getSize() - 1);

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed



        int index = this.jList1.getSelectedIndex();

        ((DefaultListModel) this.jList2.getModel()).removeElement(this.jList1.getSelectedValue());

        if (index == -1) {
            return;
        }

        // parent.controller.deleteAccount(((JAPPAccountSettings) ((DefaultListModel) this.jList1.getModel()).getElementAt(index)).accountId);

        ((DefaultListModel) this.jList1.getModel()).remove(index);

        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");

    }//GEN-LAST:event_jButton2ActionPerformed

