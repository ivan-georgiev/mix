    private void search() {

        ((DefaultListModel) this.jList1.getModel()).removeAllElements();

        String s = this.jTextField1.getText();
        String current;

        if (s == null) {
            s = "";
        }

        for (int i = 0; i < jList2.getModel().getSize(); i++) {

            current = jList2.getModel().getElementAt(i).toString();

            if (current.toUpperCase().contains(s.toUpperCase())) {

                DefaultListModel dm = (DefaultListModel) jList1.getModel();
                dm.addElement(jList2.getModel().getElementAt(i));

                if (dm.getSize() == 1) {
                    jList1.setSelectionInterval(0, 0);
                }


            }

        }
    }
